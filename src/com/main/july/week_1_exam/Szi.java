package com.main.july.week_1_exam;

public class Szi {
    private  int i;
    private int j;
    private String str;

    public Szi(int i, int j,String str){
        this.i = i;
        this.j = j;
        this.str = str;
    }
    public Szi(){

    }

    public String getStr() {
        return str;
    }

    public void setStr(String str) {
        this.str = str;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public int getJ() {
        return j;
    }

    public void setJ(int j) {
        this.j = j;
    }
}
